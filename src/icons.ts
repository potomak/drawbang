const pixelIcon = (pattern: string): string => {
  const rows = pattern.trim().split("\n");
  let rects = "";
  for (let y = 0; y < rows.length; y++) {
    const row = rows[y];
    let x = 0;
    while (x < row.length) {
      if (row[x] === "#") {
        let w = 1;
        while (x + w < row.length && row[x + w] === "#") w++;
        rects += `<rect x="${x}" y="${y}" width="${w}" height="1"/>`;
        x += w;
      } else {
        x++;
      }
    }
  }
  return `<svg viewBox="0 0 16 16" shape-rendering="crispEdges" fill="currentColor" aria-hidden="true">${rects}</svg>`;
};

